#Crie um programa que leia dois números e mostre a soma entre eles.

#Armazena o número 1
num1 = int(input('Digite o número 1: '))

#Armazena o número 2
num2 = int(input('Digite o número 1: '))

#Calcula os dois números
soma = num1 + num2

#Exibe o resultado
print(f"A soma dos dois números é igual a {soma}")
